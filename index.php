      <p>Apichart Vanavichit has a M.Sc. in plant breeding and a Ph.D. in crop science.  He was the lead Thai scientist in the team that sequenced the rice genome (IRGSP) with 9 other nations, and furthermore he established the Rice Gene Discovery and Rice Science Center to facilitate rice molecular breeding in Thailand.  His centers have led in the discovery of genes for 2-acetyl-1-pyrroline (aromatic gene), Sub1C (flash flooding tolerance), Fe-toxic tolerance, waxy, and terpene synthase (brown planthopper resistance). Significant outcomes from his centers have established a high-through-put platform for breeding-by-pyramiding MAS to improve Thai Jasmine and low GI rice to withstand flash flooding, drought, heat, salinity, diseases and insects problems.  He has pioneered a new research frontier in rice; by using fast neutron bombardment to understand how genetic variation can be induced leading to the discovery of undiscovered or novel gene functions. He is also a leader in the molecular breeding of environmentally friendly rice. His high nutrition rice which has enriched grain iron levels, a high level of antioxidants and a low glycemic index has become a new national product.  

